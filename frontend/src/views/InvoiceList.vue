<script>
import { mapGetters, mapState } from 'vuex'
import SideBar from '../components/SideBar.vue'
import HemBurger from '../components/Hemburger.vue'

export default {
  name: 'InvoiceList',
  components: {
    SideBar,
    HemBurger
  },
  data() {
    return {
      showFilters: false,
      searchQuery: '',
      dateRange: {
        start: '',
        end: ''
      },
      isLoading: false,
      sidebarOpen: true,
      isMobile: false
    }
  },
  computed: {
    ...mapGetters({
      invoices: 'invoice/allInvoices',
      filteredInvoices: 'invoice/filteredInvoices',
      currentFilter: 'invoice/currentFilter',
      availableStates: 'invoice/availableStates',
      selectedStates: 'invoice/selectedStates'
    }),
    ...mapState({
      loading: state => state.invoice.loading,
      error: state => state.invoice.error
    }),
    // minDate() {
    //   if (this.invoices.length === 0) return '';
    //   return this.invoices.reduce((min, invoice) => {
    //     const date = new Date(invoice.createdAt);
    //     return date < new Date(min) ? date.toISOString().split('T')[0] : min;
    //   }, new Date().toISOString().split('T')[0]);
    // },
    maxDate() {
      if (this.invoices.length === 0) return '';
      return this.invoices.reduce((max, invoice) => {
        const date = new Date(invoice.createdAt);
        return date > new Date(max) ? date.toISOString().split('T')[0] : max;
      }, '1970-01-01');
    },
    userGenerated() {
      return this.invoices.filter(invoice => invoice.generatedBy === 'User').length
    },
    panelGenerated() {
      return this.invoices.filter(invoice => invoice.generatedBy === 'Panel').length
    }
  },
  methods: {
    toggleSidebar() {
      this.sidebarOpen = !this.sidebarOpen;
    },
    checkScreenSize() {
      this.isMobile = window.innerWidth < 768;
      // On larger screens, sidebar should always be visible
      if (!this.isMobile) {
        this.sidebarOpen = true;
      }
    },
    setFilter(filter) {
      this.$store.dispatch('invoice/setFilter', filter)
    },

    updateSearchQuery() {
      this.$store.dispatch('invoice/setSearchQuery', this.searchQuery)
    },

    updateDateRange() {
      this.$store.dispatch('invoice/setDateRange', this.dateRange)
    },

 

    toggleState(state) {
      this.$store.dispatch('invoice/toggleState', state)
    },

    clearFilters() {
      this.searchQuery = ''
      this.dateRange = { start: '', end: '' }
      this.$store.dispatch('invoice/clearFilters')
    },

    logout() {
      this.$store.dispatch('auth/logout')
    },

    formatDate(dateString) {
      const date = new Date(dateString)
      return {
        date: date.toLocaleDateString('en-GB', {
          day: '2-digit',
          month: '2-digit',
          year: 'numeric'
        }),
        time: date.toLocaleTimeString('en-US', {
          hour: '2-digit',
          minute: '2-digit',
          hour12: true
        })
      }
    },

    viewInvoice(id) {
      this.$router.push(`/invoices/view/${id}`)
    },

    deleteInvoice(id) {
      if (confirm('Are you sure you want to delete this invoice?')) {
        this.$store.dispatch('invoice/deleteInvoice', id)
      }
    },

    editInvoice(id) {
      this.$router.push(`/invoices/edit/${id}`)
    },

    async fetchInvoices() {
      this.isLoading = true
      try {
        await this.$store.dispatch('invoice/fetchInvoices')
        console.log('Invoices fetched:', this.invoices)
      } catch (error) {
        console.error('Error fetching invoices:', error)
      } finally {
        this.isLoading = false
      }
    }
  },
  created() {
    this.fetchInvoices()
  },
  mounted() {
    // Check screen size on mount
    this.checkScreenSize();
    
    // Add resize event listener
    window.addEventListener('resize', this.checkScreenSize);
  },
  beforeDestroy() {
    // Clean up event listener
    window.removeEventListener('resize', this.checkScreenSize);
  }
}
</script>

